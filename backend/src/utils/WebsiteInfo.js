const getWebsiteInfoAnswer = (query) => {
    const lowerCaseQuery = query.toLowerCase();

    // Search FAQs
    for (const faq of websiteInfo.faqs) {
        if (faq.question.toLowerCase().includes(lowerCaseQuery)) {
            return faq.answer;
        }
    }

    // Search Privacy Policy
    for (const policy of websiteInfo.privacyPolicy) {
        if (policy.title.toLowerCase().includes(lowerCaseQuery) || policy.content.toLowerCase().includes(lowerCaseQuery)) {
            return policy.content; // Return the full content for policy questions
        }
    }

    // Search Terms of Service
    for (const terms of websiteInfo.termsService) {
        if (terms.title.toLowerCase().includes(lowerCaseQuery) || terms.content.toLowerCase().includes(lowerCaseQuery)) {
            return terms.content; // Return the full content for terms questions
        }
    }

    return null;
};

